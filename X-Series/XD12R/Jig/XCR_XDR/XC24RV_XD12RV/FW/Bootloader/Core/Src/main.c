#pragma optimize=none
static uint8_t boot_img_jump(uint32_t app_abs_addr_start)
{
    uint8_t result = 0U;
    const vector_t *vector_p = (vector_t*)(app_abs_addr_start);

    if((vector_p->stack_addr& 0x2FF00000) == 0x20000000)
    {
        result = 1U;

        MX_USART2_UART_Deinit();

        HAL_RCC_DeInit();
        HAL_DeInit();
        
        /* Disable SysTick interrupt */
        SysTick->CTRL &= ~(SysTick_CTRL_TICKINT_Msk | SysTick_CTRL_ENABLE_Msk);

        __disable_irq();                /* 1. Disable interrupts */
        
        /* initialize user application's stack pointer */
        __set_MSP(vector_p->stack_addr);    /* 2. Configure stack pointer */

        vector_p->func_p();             /* 4. Jump to application */
    }

    return result;
}

